										<span style="font-size: 12px;">I&nbsp;like&nbsp;for&nbsp;you&nbsp;to&nbsp;be&nbsp;still:&nbsp;it&nbsp;is&nbsp;as&nbsp;though&nbsp;you&nbsp;are&nbsp;absent</span>
